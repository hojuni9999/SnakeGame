    // 게임 루프
    bool gameOver = false;
    while (!gameOver) {
        clear();

        // 화면에 맵 그리기
        for (int y = 0; y < MAP_SIZE; y++) { 
            for (int x = 0; x < MAP_SIZE; x++) {
                if (map[y][x] == 0) {
                    mvprintw(y, x, " ");
                } else if (map[y][x] == 1) {
                    mvprintw(y, x, "#");
                } else if (map[y][x] == 2) {
                    mvprintw(y, x, "+");
                } else if (map[y][x] == 3) {
                    mvprintw(y, x, "O");
                } else if (map[y][x] == 4) { // 추후에 추가로 아이템 등 추가할 수 있다.
                    mvprintw(y, x, "o");
                }
            }
        }

        // 화면 업데이트
        refresh();

        // 100ms 대기
        napms(100);
    }

    // 게임 종료 후 NCurses 정리
    endwin();
    return 0;
